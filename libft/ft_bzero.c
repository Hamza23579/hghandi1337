#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t		i;
	char		*m;

	i = 0;
	m = s;
	if (n != 0)
	{
		while (i < n)
		{
			m[i] = 0;
			i++;
		}
	}
}
