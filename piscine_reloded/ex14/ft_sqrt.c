int		ft_sqrt(int nb)
{
	int		i;
	int		mid;

	mid = nb / 2;
	if (nb % 2 == 0)
	{
		i = 2;
		while ((i * i) != nb && i < mid)
			i += 2;
		if (i * i == nb)
			return (i);
	}
	else
	{
		i = 3;
		while ((i * i) != nb && i < mid)
			i += 2;
		if (i * i == nb)
			return (i);
	}
	return (0);
}
